			<typo-heading :level="1">瓶颈期：收视下降、问题初显（2008-2013）</typo-heading>
			<typo-heading :level="2">遭遇冲击：与互联网的初次对垒</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>这一时期已经进入Web2.0的小高峰，互联网的逐渐发展与普及给传统电视媒体带来了一定的冲击。根据中国互联网络信息中心（CNNIC）发布的《第22次中国互联网络发展状况统计报告》，截至2008年6月底，我国网民数量达到了2.53亿，但互联网普及率只有19.1%</span
