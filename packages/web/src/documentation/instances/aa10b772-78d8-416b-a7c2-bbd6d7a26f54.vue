				><span
					>互联网媒体的产生和发展致使传统媒体受众关注度下降。在2008年央视索福瑞提供的1月重庆地区主要频道节目收视排行中《天天630》作为唯一一个重庆本土电视民生新闻节目排名第6，平均收视率为4.95，较之6年前节目播出的第一个月的6.0还低，而到了2012年年底时《天天630》的收视率已经跌至3.5</span
				><typo-reference :order="6"
