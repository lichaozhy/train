			<typo-heading :level="2">问题初显、矛盾突出</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>这一时期的《天天630》不仅因为互联网媒体的冲击收视下降，经过几年的发展，自身存在的问题也愈发突出。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>首先是对“民生”的解读与诠释失准。虽然《天天630》一直将自身定位为服务社会中下层普通民众的新闻节目，但这一时期不少人对这个节目的评价并不高。《天天630》的新闻，内容多集中在车祸、房屋纠纷、垃圾、水、电、气等方面，且以负面新闻居多，甚至有网友戏称“天天630，全是小事情，大事不敢播，小事整不停，井盖被偷盗，水管遭挖爆，一个马蜂窝，都要紧到播。”这反映出《天天630》对“民生”概念的理解存在局限性。在信息传播中，受众的认知判断很容易受外界意见的影响，但作为主流新闻媒体，对民生的选取和表达却必须在贴近百姓的同时又必须保持其独立性。《天天630》对民生概念的狭隘理解限制了其对民生的概念表达和诠释视角，导致节目内容和形式比较单一，缺乏多样性和创新性。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>其次是存在对重庆农村地区报道的缺失。重庆作为一座“大城市”与“大农村”结合的城市，相较于其他三座直辖市拥有着数量庞大的农村人口，但囿于这一时期没有那么发达的交通条件与通信技术，《天天630》对重庆农村地区的报道并不多，即使偶有涉及，被展示的农村面貌也总是贫穷落后、破败不堪。“取悦”城市忽略农村的错误倾向，不仅造成了新闻报道的失衡，流失了大量的农村观众，影响力下降，对媒体的发展以及社会的和谐稳定也会有所阻碍。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>最后是这一时期《天天630》日益突出的广告收益与节目收视之间的矛盾。2009年《天天630》的节目时长从60分钟延长到了90分钟，而随着节目时长的增加，节目间广告也在不断变多。本来电视媒体通过提供新闻内容服务，吸引电视观众的注意力，同时通过销售广告时间，将受众市场销售给广告商来增加收益也无可厚非。但电视台缺乏了对广告内容的审核，这一时期穿插在《天天630》中间的广告不少都非常低俗，受众难以接受。在下班高峰期，乘坐公交车的人们通过车载电视经常会听到《天天630》插播的医院广告，“我又怀孕了”“烦死了，烦死了，人家还没想要嘛”……这些低俗的广告词给受众造成了严重的精神污染，产生了不良的社会效应。</span
				></typo-paragraph
			>
