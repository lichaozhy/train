			<typo-heading :level="1">案例启示：新华日报社国际传播实践的可借鉴性</typo-heading>
			<typo-heading :level="2">内容挖掘+技术赋能：用创意打造海外用户喜闻乐见的产品</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>在信息量空前“爆炸”的万物皆媒时代，用户面临的信息选择多了，也迎来了认知负荷、信息超载的困难，如何实现与海外受众的有效连接，深耕内容是最根本的。新华日报社在国际传播融媒产品生产过程中，不断挖掘中华文化的国际传播价值，试图找到文化的世界共同性、文化的价值内涵以及地域文化的独特性。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>例如在《Home Story in
					Jiangsu（“家”在江苏）》作品中，一方面借用拼音“JIA”与甲骨文“家”设计IP形象和互动游戏，另一方面通过展现中国现代温情故事如“一碗汤的距离”，挖掘中国传统核心价值观和家文化价值理念，让国际受众了解中国家庭的和睦、孝顺等传统美德；此外，作品还深入挖掘像苏州评弹、书法和围棋等传统艺术的当代发展。《“嗨，你有一个来自江苏的漂流瓶”》作品则在内容挖掘的基础上进一步创新，瓶子既是实体的也是虚拟的，在现实世界和虚拟世界同步传播，让世界范围内的网友足不出户就可以参与到讨论中来，倾听参与者和建设者的故事，带来中外人士的祝福。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>在数字时代，想要在网络空间获得广泛而深入的传播效果，运用技术赋能是至关重要的。在新华日报社的三例传播实践中，都采用了线上H5超文本互动语言技术，以线下发起的活动为素材来源，构建了一个虚拟化的叙事空间。H5技术能够创建交互时间线，让受众通过滑动页面更直观地了解时间脉络。同时，利用大量互动设计引导受众参与新闻互动，在互动中主动获取信息。此外，XR技术的运用将真实与虚拟相结合，打造了一个可人机交互的3D场景，为体验者带来虚拟与现实之间无缝转接的“沉浸感”。</span
				></typo-paragraph
			>
			<typo-heading :level="2"
				>借海出船+借梯上楼：让海外用户能够听得到、听得清、听的全</typo-heading
			>
			<typo-paragraph :indent="2"
				><span
					>党的十八大以来，习近平总书记就推进国际传播能力建设提出了一系列新理念新思想新战略，其中就包括“有理说不出、说了传不开”的局面必须扭转。近年来，新华日报社创造性地借力谋事，进而抢占先机，掌握舆论主动权，有效地抵达国外舆论场。例如，新华日报社先后与美国《侨报》、《欧洲时报》、韩国《全北道民日报》《中央日报》、加拿大《加中时报》《环球华报》、埃及《非洲华侨周报》等众多媒体网站建立合作关系，设立新闻专版发布江苏信息。但“借船”和“借梯”不是简单地在国外版面刊登有关江苏的稿件，而是要精准传播，在恰当的时间，以恰当的产品和方式，推送给恰当的受众。</span
				></typo-paragraph
			>
			<typo-heading :level="2">差异叙事+柔性传播：用世界语讲好中国故事</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>新时代中国的国际传播需具备全球性的价值取向，但这并不意味着在对外的传播过程中要一味地获取对外认同，纳入他国话语体系进行传播，而是应该在传播议题中找到全球性内容，将中国智慧、中国方案和中国故事融入国际话语体系，例如作品《在中国，看Frank打开长三角“盲盒”》在策划与实施中坚守政治高度，讲好中国式现代化故事，以六个原型人物为依据，探讨产业创新、绿色发展等中国特色议题，从侧面展现“以人为本”“追求人民美好生活”发展理念生动实践。差异叙事还体现在对国外受众文化习惯和审美偏好的尊重，新华日报社在实践过程中设置多元话语体系，以不同的语言抵达不同的受众，实现精准传播。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>宏大叙事难以唤起个体情感体验，在跨文化传播中要关注不同国家作为世界公民的相似性，即从“人性”和“普通人地日常生活”出发，以真实动人的故事构建与社会、历史和文化的勾连。“Home
					Story in
					Jiangsu——外国人眼里的‘江苏这十年’”项目虽以中国发展这10年为宏大观照，但视点下沉，关注点微观，选取“家”为主意象。与之类似，另外两例分别以世界共通的“盲盒文化”“漂流瓶”引发人们兴趣，实现破圈。</span
				></typo-paragraph
			>
			<typo-heading :level="2">他者视角：展现客观真实的中国形象</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>在新华日报社的三例传播实践中都有一个有趣的现象，即故事的讲述者都出现了在中国生活的外国人身影。国之交在于民相亲，外国友人、留学生群体都是潜在的民间跨文化使者。他们的讲述、口口相传和社交媒体言论也会让更多的人乐意了解、分享新闻产品。此外，采用他者视角去讲述中国故事，对于国外受众来说，更具备亲近性、客观性和说服力，往往会起到更好的传播效果。</span
				></typo-paragraph
			>
			<typo-heading :level="2">结语</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>在全球化背景下，做好国际传播，既是省级主流媒体的思考题，也是必答题。解题过程中，省级党媒不仅要有大局意识，深刻把握国际传播新要求，还要立足柔性传播和精准传播，勇于传播破圈，打造“新闻作品+文化IP+平台化运作”的深度融合新模式，进而提升自身国际传播力和影响力。讲好中国故事，媒体一直在路上！</span
