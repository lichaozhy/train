			<typo-heading :level="1">案例启示：当前地方主流媒体国际传播SWOT分析</typo-heading>
			<typo-heading :level="2">优势Strengths</typo-heading>
			<typo-heading :level="3">家底丰厚——丰富璀璨的地域人文文化</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>中国地大物博，有着极为丰厚的人文家底，例如以区域地理环境、特有自然资源为主的地域特色；以历史文化、传统特色、民间艺术、美食为主的地方风俗；以地区政治名人、学者、领域专家、艺术家、企业家为主的名人效应；以影视、动画、游戏、创意产业为主的文化产业。如此丰厚的地域文化资源有助于增强文化符号标识性，形成有记忆点、易于传播的文化概念，还可以由此打造地域文化IP，强化品牌运营，助力文化扬帆出海。</span
				></typo-paragraph
			>
			<typo-heading :level="3">通宏洞微——自下而上的实践优势</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>读懂中国，关键在于要读懂中国式现代化。不同于西方以物质主义为基础的“物”的现代化，中国式现代化以“人”的发展为价值导向，代表了最广泛的人民利益，其中所蕴含的价值理念集中体现在正在发生的地方实践。因此做好中国式现代化国际传播需要深入基层，展开扎实的调查实践，充分挖掘典型事例，见人见事见生活。地方主流媒体在基层的接近性和参与性上有着天然优势和更为顺畅的发挥空间，相较于自上而下根据理念寻找案例，自下而上是体现价值的过程，更易受到国际传播受众的青睐。</span
				></typo-paragraph
			>
			<typo-heading :level="2">不足Weaknesses</typo-heading>
			<typo-heading :level="3">掩暇藏疾——太过完美反而不完美</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>习近平总书记强调：“讲好中国故事，传播好中国声音，展示真实、立体、全面的中国，是加强我国国际传播能力建设的重要任务。坚持问题导向是习近平新时代中国特色社会主义思想世界观和方法论的重要内容之一。然而，部分地方主流媒体在国际传播工作中“只报喜不报忧”，对发展中的问题支支吾吾甚至刻意隐瞒，这种做法不仅容易引起猜测和误会，更可能损害国家形象，使传播效果大打折扣。这对地方主流媒体“把关人”的职业素养提出了更高的要求，一个创作者不能只简单进行正面或负面分类，也应该在细节中看到不足的地方，只有这样才有产生进步的可能。</span
				></typo-paragraph
			>
			<typo-heading :level="3">小马拉大车——专业国际传播队伍不完备</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>与北京、上海、广州等众多一线城市相比，众多三四线的地方主流媒体国际传播力不足，究其根本，主要是因为地方主流媒体所拥有的专业人才无法满足国际传播的需求，那些精通外语、熟知国际形势、具备采编能力的“一专多能”的人才仍较为短缺。</span
				></typo-paragraph
			>
			<typo-heading :level="3">传者中心论——受众定位模糊，连接不深入</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>众多地方主流媒体在国际传播过程中仍存在受众模糊定位的问题。从语言上看，很多媒体的海外社交媒体平台以英语传播为主，语种较为单一，难以适应面向全球尤其是“一带一路”共建国的需求；从平台上看，目前海外传播的运营平台仍集中在Twitter、Facebook、Instagram以及YouTube上，而其中存在风险，尤其是近年来部分画外社交媒体对中国媒体采取限制措施，审查内容、限制推广甚至直接封禁已是家常便饭；从受众圈层来看，部分媒体在国际传播工作中仍秉持“大众传播”思维，缺乏“出海破圈”的清晰思维。</span
				></typo-paragraph
			>
			<typo-heading :level="3">机会Opportunities</typo-heading>
			<typo-heading :level="3">国家撑腰的底气——中国国际地位的提升和国家政策的支持</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>中央领导集体锐意进取和创新，其治国理念和治国能力受到国际社会的广泛认可，同时随着中国国力日益强大和在国际事务中发挥重要作用，世界上越来越多的国家对中国表示认可、赞扬，中国获得了越来越多的关注，越来越多的国际会议、赛事等在中国落地，这为国内媒体对外传播创造了外部环境和条件。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>此外，国家重要战略部署的提出以及国内环境的稳定有助于媒体做好国际传播工作。对新华日报社来说，江苏是国家重大发展战略的叠加区域，在长三角一体化、江苏沿海地区发展、苏南现代化建设示范区等战略深入实施之际，“一带一路”和长江经济带战略又一次在江苏省叠加。特别是国家实施“一带一路”倡议，作为丝绸之路经济带和21世纪海上丝绸之路交汇点的江苏，在国际交流合作中的地位更加显要。这些重要的国家战略布局，为媒体的国际传播工作提供了难得的历史性机遇。</span
				></typo-paragraph
			>
			<typo-heading :level="3">智库力量推动研究成果转化，专业指导成为现实</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>2015年1月中共中央办公厅、国务院办公厅印发《关于加强中国特色新型智库建设的意见》，对中国特色新型智库建设进行了全面部署。围绕如何讲好中国故事、传播好中国声音，众多知名高校、科研院所等智库单位持续探索，取得了一系列研究成果，这为媒体壮大国际传播人才队伍，获得三方意见和指导提供了机遇。</span
				></typo-paragraph
			>
			<typo-heading :level="3">技术赋能国际传播工作，谁来说、怎么说成为吸引点</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>国际传播要让海外用户听得懂、听得进，需要构建融通中外的话语体系，其中“谁来说”“怎么说”是关键要素。随着全球化的发展，越来越多的外国人选择到中国居住、工作，他们以自己个性化的视角分享眼中的中国更加真实、客观，因而更具有说服性。此外，数智时代的到来，5G、人工智能等信息技术为创新国家传播方式，提升信息传播触达率和影响力提供了技术支持，</span
				></typo-paragraph
			>
			<typo-heading :level="3">挑战Threats</typo-heading>
			<typo-heading :level="3">社会意识形态不同，刻板印象难以消解</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>马克思主义唯物史观认为，社会存在决定社会意识。相较于西方社会注重个体权益的表达，中国社会意识更注重集体观念，即个体在集体中的义务表达，因此在国内觉得感动无私的正能量故事在西方视角里并不能共情，甚至觉得是无意义的。此外，部分国家仍遗存着对共产主义意识形态的成见，保持着对中国的刻板印象。</span
				></typo-paragraph
			>
			<typo-heading :level="3">国际社会仍存在不利我因素，舆论局势“西强我弱”</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>当今时代，西方国家掌握着全球90%以上的信息传播，借助媒介霸权对中国进行抹黑，在民族宗教、领土争端、公共安全等问题上进行不实报道，使得海外用户看到的多是中国被歪曲和遮蔽的形象，加深了他们对中国的误解。</span
				></typo-paragraph
			>
