				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>我们做什么？回答了党媒的发展方向与路径问题。太仓日报致力于把报纸变为平台，把资讯变为服务，把读者变为用户，探索“互联网+新闻+服务”的商业模式。</span
