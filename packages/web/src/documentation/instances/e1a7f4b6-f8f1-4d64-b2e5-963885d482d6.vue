			<typo-heading :level="1">理论依据与分析</typo-heading>
			<typo-heading :level="2"
				>太仓日报、太仓市广播电视总台等传统媒体为什么需要转型与融合？</typo-heading
			>
			<typo-heading :level="3">知识点</typo-heading>
			<typo-paragraph :indent="2"><span>媒体融合动因</span></typo-paragraph>
			<typo-heading :level="3">理论依据</typo-heading>
			<typo-paragraph :indent="2"><span>媒体融合动因理论模型</span></typo-paragraph>
			<typo-paragraph :indent="2"
				><span
					>黄金（2011）在《媒介融合的动因模式》一书中，将媒介融合动因的理论模式分为经济因素主导的媒介融合、政治因素主导的媒介融合、数字技术主导的媒介融合、社会与文化主导的媒介融合四种。他认为探讨媒介融合的动因不仅要全面，而且要考虑这些动因之间的关系，因为只有这些关系协调得当才能形成有效合力，推动融合的开展；而协调不当则会削弱主导动因的影响力，阻碍融合的开展。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>与动力机制相对的是一种阻力机制，构建动力关系学可以用于分析当下的媒介融合现状，从而发现其问题所在。以一种理论模式来指导融合实践，特别是分析中国目前的媒介融合现状，预测中国未来媒介融合的发展方向，是有一定实践意义的。</span
				></typo-paragraph
			>
			<typo-heading :level="3">案例分析</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>面对新媒体时代传统媒体面临的种种挑战，太仓日报在多年媒体实践中认识到，要在新时代保持竞争力，要首先认识到自己的责任，提升新闻供给质量，实现媒体融合创新，打造新型主流媒体。美国学者罗杰·菲德勒在《媒介形态变化》一书中写道：传播媒体的形态变化，通常是由于可感知的需要、竞争和政治的压力，以及社会和技术革新的复杂相互作用引起的。我们可以借助这一观点讨论太仓市融媒体中心媒体融合创新的动因。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"><span>内部因素：</span></typo-paragraph>
			<typo-paragraph :indent="2"><span>（1）管理认知</span></typo-paragraph>
			<typo-paragraph :indent="2"
				><span
					>太仓日报社和太仓市广播电视总台在组织结构设置、人员管理、考核机制构建等方面均存在一些问题，无法紧跟时代，充分利用内部资源实现媒体融合发展。因此，管理层内部意识到，在媒体融合方面，太仓日报需要创新内部运行机制，重组媒体生产架构。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"><span>（2）资源能力</span></typo-paragraph>
			<typo-paragraph :indent="2"
				><span
					>作为当地的权威主流媒体，太仓日报不是信息搬运工，而是内容供应服务商，能够让内容抵达用户的内心深处，满足用户对优质内容的需求。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"><span>（3）盈利模式</span></typo-paragraph>
			<typo-paragraph :indent="2"
				><span
					>随着互联网的快速发展拓宽了用户选择，传统媒体的盈利不断减少。新媒体与自媒体行业的崛起带给太仓日报社和太仓市广播电视总台很大的冲击，传统媒体观众大量流失，业绩下滑明显，市场份额缩减，广告业务模式已经无法维持自身生存。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>节目发布后相关内容数量会出现明显上升趋势，这表明节目本身对与“三磅宇宙”相关的讨论具有显著的推动作用。除了观众的直接关注和讨论外，节目发布同时会引发一系列连锁反应，带来不同媒体的转发报道、社交平台用户关注等，这些都在一定程度上推动了相关内容量的激增。节目带来的持续影响使得峰值并非只涵盖节目发布当天，而是会持续一系列相关活动共同影响的一段时间。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"><span>外部因素：</span></typo-paragraph>
			<typo-paragraph :indent="2"><span>（1）技术动因</span></typo-paragraph>
			<typo-paragraph :indent="2"
				><span
					>技术的发展是媒体融合的直接动因。人类传播史上每一次媒介融合都被打上技术的烙印。当代数字技术和互联网通讯技术使不同媒体可以在统一平台进行新闻生产与传播，最终由媒体业务的融合进而拓展到媒体所有权、组织、制度乃至产业的深度融合。尼葛洛庞帝曾阐述比特与原子的区别，在媒体领域此前由于技术欠缺，以原子为单位的传统媒体无法在一个平台上深度融合，如今技术条件可实现文字、图片、音频、视频比特化，促使媒体融合的全方位推进。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"><span>（2）经济动因</span></typo-paragraph>
			<typo-paragraph :indent="2"
				><span
					>经济因素是促使媒体融合的根本动因。第39次《中国互联网络发展状况统计报告》显示：截至2016年12月，我国网民规模达7.31亿，手机网民规模达6.95亿，占比达95.1%。当今用户出现明显数字化、分众化和移动化需求，传统媒体受众流失、广告投放量下滑乃至濒临破产，急需通过借力新媒体进行转型。于新媒体而言，其初期建设成本高昂、采编团队专业化程度低、缺乏品牌优势等也使新媒体需要融合传统媒体来提示实力。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"><span>（3）政治动因</span></typo-paragraph>
			<typo-paragraph :indent="2"
				><span
					>国内政治与国际政治的客观需求是媒体融合的重要动因。《中共中央关于全面深化改革若干重大问题的决定》指出，整合新闻媒体资源，推动传统媒体和新兴媒体融合发展。《关于推动传统媒体和新兴媒体融合发展的指导意见》指出，推动传统媒体和新兴媒体在内容、渠道、平台、经营、管理等方面的深度融合。无论传统媒体或新媒体其本质都是党和人民的喉舌，媒体融合不仅事关媒体业存亡，更关系到党的执政根基和社会话语权的掌握。同时，全球一体化进程加快为媒体提出了新的要求。媒体融合是国家软实力的组成部分，也是国家硬实力的重要支撑。通过媒体融合，可以生产优质多样的新闻产品，向境外传递中国声音、塑造中国形象、彰显中国精神。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"><span>（4）社会动因</span></typo-paragraph>
			<typo-paragraph :indent="2"
				><span
					>社会动因是媒体融合的另一重要动因。德国哲学家哈马贝斯曾提出公共领域概念，认为媒介是公共领域的重要组成部分。郑永年技术赋权理论提出，互联网赋予草根公民虚拟网络空间中自由参政议政的更大权力。当今融媒体以开放的平台和快捷的互动让公民民主参政权得以平移至线上，利用互联网和反应社情民意或监督政府行为。此外，公众对新闻由单纯接收需求升级为能动选择乃至参与生产。微博、微信等社交媒体作为媒体融合的产物，全方位迎合了用户的选择意识、在场意识和参与意识，为UGC生产创造了条件。</span
				></typo-paragraph
			>
			<typo-heading :level="2">太仓日报是如何定位的？为什么要成立太仓市融媒体中心？</typo-heading>
			<typo-heading :level="3">知识点</typo-heading>
			<typo-paragraph :indent="2"><span>媒体融合、媒体融合创新</span></typo-paragraph>
			<typo-heading :level="3">理论依据</typo-heading>
			<typo-paragraph :indent="2"><span>（1）媒体融合内涵</span></typo-paragraph>
			<typo-paragraph :indent="2"
				><span
					>媒体即传播信息的载体，包括书籍、报纸、广播、电视等。媒体融合（Media
					Convergence）这一概念最早由美国麻省理工大学的Ithiel De Sola
					Pool提出。1983年，他在其著作《自由的科技》中提出了“传播形态融合”（The Convergence of
					Modes），指各种媒介呈现出多功能一体化的趋势。他还指出，这是一种正在“模糊媒介间界线”的过程，甚至涉及点对点通信和大众传播。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>中国学者韦路将媒体融合定义为人类传播活动诸要素内部界限模糊的一种状态，这些要素包括技术、经济、主体、内容、规范等。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>第一，媒体融合是一个多层面的概念，不仅仅限于技术层面。人类传播活动关涉的所有方面，技术、经济、主体、内容、规范等，都是媒体融合的重要部分。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>第二，媒体融合是一个多视角的概念。其本意不光是传统媒体拥抱新兴媒体，反之亦然，甚至也包括传媒之间和新兴媒体之间的融合。同时，媒体融合不仅可以从媒体来看，也可以从人的角度来看，包括生产者、消费者和生产消费者。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>第三，媒体融合是一种界限模糊的状态。在相当长的时间里，各种新旧媒体会并存，只是界限日益模糊，使得媒体真正成为随时随地的存在。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>第四，媒体融合是一种不断变化的状态。关于这种程度的变化，习近平关于媒体融合的一个比喻极为形象。2016年2月19日，在党的新闻舆论工作座谈会上，习近平指出，要“尽快从相‘加’阶段迈向相‘融’阶段，从‘你是你、我是我’变成‘你中有我、我中有你’，进而变成‘你就是我、我就是你’。”新旧媒体的相对性也意味着媒体融合是一个永无止境的过程。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>2014年8月18日，中央全面深化改革领导小组第四次会议通过《关于推动传统媒体和新兴媒体融合发展指导意见》，传统媒体已经习惯于多年来形成的媒体治理理念、媒介运营模式和固化的机制体制，对移动互联时代的思维方式、融合路径、融合关键等核心问题缺乏清醒的认识和掌握。当时，国内传统媒体与新兴媒体融合，基本上有三种路径：</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>第一种是固守内容优势，认为自身属于传统媒体，有内容优势，便将现成的内容直接放在新媒体平台上，认为这种方式就是融合。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>第二种是主业（广告收入）开始滑坡，从而寻找多元产业支撑，即拓展其他产业以获得较好收益来支撑传媒主业，例如开拓房地产、展会等。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>第三种是面对互联网冲击，彻底改变自己的思路与做法，重新架构自己的生产流程，彻底拥抱互联网。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"><span>（2）媒体融合创新</span></typo-paragraph>
			<typo-paragraph :indent="2"
				><span
					>融合创新，是指美籍奥地利经济学家熊彼特1912年在其名著《经济发展理论》中首次系统提出的创新理论。融合创新是将各种创新要素通过创造性地融合，是各创新要素之间的相互补充、相互匹配，从而使创新系统的整体功能发生质的飞跃，形成独特的不可复制、不可超越的创新能力和核心竞争力。该理论重点关注从“创新性破坏”中寻找融合发展之路。</span
				></typo-paragraph
			>
			<typo-heading :level="3">案例分析</typo-heading>
			<typo-paragraph :indent="2"
				><span
					>面对纸媒的每况愈下，太仓日报在多年的媒体实践中认识到，要在新时代保持竞争力，要首先认识到自己的责任，提升新闻供给质量，打造新型主流媒体。加上2017年党的十九大作出了实施乡村振兴战略的重大决策部署。在此背景下，太仓日报提出并回答了“融合三问”。</span
