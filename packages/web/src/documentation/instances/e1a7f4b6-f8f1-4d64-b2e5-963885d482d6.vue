			<typo-heading :level="3">知识点</typo-heading>
			<typo-paragraph :indent="2"><span>融合文化理论</span></typo-paragraph>
			<typo-heading :level="3">理论依据</typo-heading>
			<typo-paragraph :indent="2"><span>融合文化理论</span></typo-paragraph>
			<typo-paragraph :indent="2"
				><span
					>融合文化理论具体的论证语境是美国当代知名的媒介和文化研究学者亨利·詹金斯提出的媒体融合论。他认为，融合的发生并不是依靠媒体设施，无论这些设施变得如何高度精密复杂。融合发生在每个消费者的头脑中，通过他们与其他人之间的社会互动来实现，媒体融合不能只聚焦技术而忽视文化。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>媒介融合理论刚刚传入中国时，有学者认为媒介融合就是“不同媒介形态集中到一个多媒体数字平台上”，并为这种“媒介大融合”描画了一个“分层次、分阶段”的路线图。第一阶段是传统媒体（报纸、广播、电视、杂志）与新媒体（互联网、手机）之间在内容和营销领域的互动与合作。第二阶段是新旧媒体在组织结构上的融合。最后阶段是“网络、媒体、通信三者的‘大融合’，打造全新的融多种媒体形式于一体的数字媒体平台”。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>在21世纪初，詹金斯就对这种简单化的、大一统式的媒介融合理念提出了强烈异议。他在2001年的一篇专栏文章中写道：历史告诉我们，“旧的媒介永远不会死”。为了阐明这一观点，他区分了媒介介质和媒介交付技术这两个不同层面。比如，录音是一种媒介，CD、MP3文件和8轨磁带则只是交付技术。交付技术会更新换代，但介质却会持久地嵌入我们的信息娱乐系统。一个介质的内容、受众和社会地位会发生改变，但介质本身却会在媒介生态系统中继续发挥作用。因此，数字媒介自然也不可能是传统媒介的“终结者”。詹金斯引用历史学家吉特曼（Lisa
					Gitelman）的观点指出，媒介一方面是一种协助交际的技术，同时也是围绕特定技术滋生出的一套社会和文化实践，认为媒介融合就是新媒介取代旧媒介的人只看到了媒介技术层面的变革，却忽视了媒介的文化层面。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"
				><span
					>詹金斯特别强调，在可预见的将来，融合不可能是一种有序而彻底的整合，而是会呈现出临时起意、将就对付、杂乱无章的特点，即融合的过程中将会出现诸多错综复杂的矛盾和悖论。伴随着媒介集中和分散的两种对立趋势，媒介行业和受众似乎都在一方面获得权力，另一方面又在失去权力。首先，媒介所有权的整合并不像说起来的那么容易。由于各部门之间缺乏有效的合作，媒体机构之间的融合往往达不到预期的效果。其次，媒体内容生产者和消费者有时会相互合作，有时则会发生冲突。由于融合对于媒体内容生产者来说既是机遇也是风险，他们可能时而鼓励消费者的参与，时而又抗拒这种参与。生产者所发出的这种杂乱信号，反过来又会让消费者感到无所适从，无法了解自己到底可以享有何种方式的、多大程度上的参与权。</span
				></typo-paragraph
			>
			<typo-paragraph :indent="2"><span>案例分析</span></typo-paragraph>
			<typo-paragraph :indent="2"
				><span
					>太仓市融媒体中心作为本土的权威主流媒体，不仅具有内容优势和公信力等声誉资源，其可撬动的平台资源也极其广阔。其县级媒体融合之路首先实现新旧媒体形式上的融合，创新内部运行机制，重组媒体生产架构；其次借力政务平台，全方位扩展新媒体业务，打开互动式融合发展的新空间；在此基础上，太仓市融媒体中心尝试自建平台，研发打造了自主可控的县级乡村振兴大数据中心，并取得了一定的成效；同时，太仓市融媒体中心用融媒体整合营销的方式，聚合各类资源，试水教育、房产、旅游等市场，探索跨界合作融合与报业经济多元经营道路，为新型党媒建设提供了经济支撑。太仓市融媒体中心在媒体融合的实践过程中，不仅注重技术上的融合，而且紧贴社会和文化实际，积极响应2017年党的十九大作出的实施乡村振兴战略的重大决策部署，在媒体融合的过程中做乡村振兴战略的传播者、引导者、参与者、推动者。</span
