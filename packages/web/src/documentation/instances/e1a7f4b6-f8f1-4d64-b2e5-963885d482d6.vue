			>
			<typo-paragraph :indent="2"
				><span
					>我们是什么？回答了党媒的角色定位问题。太仓日报明确了自身在本土第一传媒、第一智媒、第一数据服务商的定位，着力打造新时代的市委机关报、全方位开疆拓土的新型党媒和报网端微号融合的权威主流媒体。</span
