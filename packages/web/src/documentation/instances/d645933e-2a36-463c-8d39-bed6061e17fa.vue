					>从文化叙事角度来看，《中国神话》选取了《补天》《逐日》《奔月》等经典神话故事作为蓝本，这些故事扎根于中华民族深厚的文化土壤，蕴含着远古人民对世界起源、自然现象、人类命运的朴素认知和浪漫主义想象，承载着中华民族的精神特质，例如女娲补天中展现的担当与拯救精神，夸父逐日中展现的坚忍不拔与追求光明的意志，嫦娥奔月中展现的对未知的向往与牺牲精神等。中华民族的传统美德和品质都蕴含在其中，基于这些耳熟能详的中国神话故事，以现代的叙事节奏和编排手法用短剧的形式呈现，这些古老的故事在新时代焕发生机，有助于强化年轻一代的观众的文化认同感与归属感。在叙事结构上，《中国神话》采用了简洁明快又跌宕起伏的节奏。在有限的时长内，精准把握故事的起承转合，开篇迅速引入神话故事的背景和关键冲突，吸引观众的注意力，例如在《治水》一集中，开篇就展现了古代洪水对当时人民生活的危害，为故事发展做好铺垫，引发观众对其背后故事的好奇。</span
