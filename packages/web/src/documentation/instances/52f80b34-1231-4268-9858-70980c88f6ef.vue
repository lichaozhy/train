			<typo-paragraph :indent="2"
				><span
					>一是善讲“故事”。习近平总书记强调：“要立足中国大地，讲好中国故事。而讲好中国故事不再是国际传播的专属，即便是对内传播媒体依旧要讲好中国故事。”“讲故事”实质上是以具体事例阐述抽象道理。《嘲讽“小镇做题家”是一个危险信号》一文讲述了杭州导游“小黑诸鸣”和安徽快递小哥李庆恒的鲜活故事，从侧面印证和强调了文章的主旨观点。此外，在文章涉及到主要领导的政务活动时，“浙江宣传”也会辅以故事化的叙事解读活动深意，以趣味化、通俗化的故事实现沟通情感和形成认同的效果。
				</span></typo-paragraph
