					>二是善用“网梗”。网梗来源于人民群众的互联网实践，在很大程度上反映了民心，甚至体现出社会焦虑和精神痛点。“浙江宣传”在很多文章中都巧妙地借用或是化用网梗，关注老百姓日常生活中的问题和困惑，使权威声音直抵人心。具体来说，“浙江宣传”的表达风格与时俱进，善于把握网络新媒体的传播规律，善于化用当下群众流行的语言，例如《“低级红”“高级黑”的六种形式》一文借用网梗，通过使用“厅局风穿搭”“赢麻了”“下大棋”等语料，增加了文章的趣味性和可读性，也使得主旨思想更易深入人心。又例如，《他们，不该是“演员”》一文化用网梗，写道：“宇宙的尽头不是考编，宣讲的终点也绝不是比赛，宣讲员的主要任务更不是‘表演’。”
