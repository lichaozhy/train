			<typo-paragraph :indent="2"
				><span
					>习近平总书记指出：“好的新闻报道，要靠好的作风文风来完成。”从态度上看，“浙江宣传”创刊词《我们来了》一文即写道：“在表现形式和风格上遵循互联网传播规律，不绕弯子、不卖关子。”总编辑李攀直言：“从上线第一天开始，‘浙江宣传’就特别重视改进文风。”从实践上看，“浙江宣传”的文风既正式又亲民，实现了在保持官方权威性的同时，用接地气的语言与读者建立情感连接。这主要体现在三个方面。</span
