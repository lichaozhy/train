					>三是用户至上。一方面，用户至上的思维体现在内容创作上的真诚。李攀将移动互联网的传播规律概括为“用户至上、情绪传播”。这就要求政务新媒体在传播实践中要注重尊重用户感受，引发用户共鸣。“浙江宣传”能够做到以平等、真诚的态度设身处地地理解网民。《历史不会浓缩于一个晚上》一文写道：“愤怒完全可以理解，因为被点燃的是广大国人最朴素的爱国情绪。”这极大引发了用户共鸣。有跟帖表示，感谢“浙江宣传”理解国人最朴素的爱国情绪。另一方面，用户至上的思维体现在话语表达上的创新。“浙江宣传”在创新话语表达方面，一是“讲白话”。在移动互联网时代快节奏的背景下，政务新媒体的观点立场应该旗帜鲜明地展现出来，以高度凝练而简洁有力的文字让读者看得明白。“浙江宣传”把文件语言、专业术语转化为平实的“白话”，使之通俗易懂，更具可读性。二是“出金句”。金句，即有新意有温度，让人读起来眼前一亮，而非“车轱辘话”的句子。善写善用金句有助于文章思想更加深入人心和广为流传。目前，“浙江宣传”文章目前已结集出版了三套书——《笔墨当随时代》《与时代肝胆相照》《一颗文心济时代》，书中每篇文章都划出了金句。这深刻反映出“出金句”对于政务新媒体创新话语表达的重要性。
